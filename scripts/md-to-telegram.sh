#!/usr/bin/env bash
#
# md-to-telegram.sh — 브리핑 마크다운을 "텔레그램 HTML"로 변환(가독성용).
#
#   텔레그램은 HTML 서식을 받을 때 & < > 3글자만 이스케이프하면 되고,
#   <b>(굵게)·<i>(기울임)만 지원한다(※ 글자 크기를 키우는 header 태그는 없음).
#
#   1단계(awk) — 가독성용 여백:
#     · 연속된 빈 줄은 하나로 정리.
#     · 섹션 제목(##…) 뒤에 빈 줄 하나(구분선은 넣지 않는다 — 줄바꿈만).
#     · 이슈/종목 헤드라인 앞에 빈 줄을 넣어 딱 붙지 않게 띄운다
#       (시장 동향 이슈=번호 이모지로 시작 / 관심·보유 종목="**종목**:" 형태).
#   2단계(sed) — 마크다운 → 텔레그램 HTML:
#     · & < > 를 먼저 이스케이프(우리가 넣을 태그보다 먼저),
#     · 줄머리 "- " 불릿과 인용부호(>)는 제거,
#     · 제목·**강조** → <b>굵게</b>,  _기울임_ → <i>.
#
#   표준입력(stdin)을 받아 변환 결과를 표준출력(stdout)으로 낸다.
#
# 사용:
#   awk ... "stock/briefings/{날짜}.md" | ./scripts/md-to-telegram.sh | ./scripts/notify-telegram.sh
#
# 참고(macOS BSD sed 호환): \b(단어경계)·비탐욕 수량자를 쓰지 않는다.
set -euo pipefail

awk '
  BEGIN { prev = "" }
  {
    # 연속 빈 줄은 하나로
    if ($0 == "") { if (prev == "") next; print ""; prev = ""; next }
    # 섹션 제목(##, ###…): 앞에 빈 줄 + 굵게(구분선 없이) + 아래 빈 줄
    if ($0 ~ /^##/) {
      if (prev != "") print ""
      print $0
      print ""
      prev = ""
      next
    }
    # 최상위 제목(#): 굵게만, 앞에 빈 줄만 보장
    if ($0 ~ /^#/) {
      if (prev != "") print ""
      print $0
      prev = $0
      next
    }
    # 이슈/종목 헤드라인 앞에 빈 줄로 띄우기(직전이 빈 줄이 아니면)
    #   - 시장 동향 이슈: 번호 이모지(1️⃣2️⃣…)로 시작 → 줄머리가 숫자
    #   - 관심/보유 종목: "**종목**: …"(또는 옛 "- **종목**") 형태
    if (($0 ~ /^[0-9]/ || $0 ~ /^- \*\*/ || $0 ~ /^\*\*[^*]+\*\*:/) && prev != "") print ""
    print $0
    prev = $0
  }
' | sed -E '
s/&/\&amp;/g
s/</\&lt;/g
s/>/\&gt;/g
s/^&gt;[[:space:]]?//
s/^- //
s/^#+[[:space:]]*(.*)$/<b>\1<\/b>/
s/\*\*([^*]+)\*\*/<b>\1<\/b>/g
s/_([^_]+)_/<i>\1<\/i>/g
'
